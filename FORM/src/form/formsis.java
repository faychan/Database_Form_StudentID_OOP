    private void selectData() {
        String C[] = {"NIS", "Name", "Gender","Birthplace", "Birthdate", "Grade", "Email", "Address"};
        DefaultTableModel dtm = new DefaultTableModel(null, C);
        String SQL = "SELECT * FROM t_siswa";
        ResultSet rs= connectDB.executeQuery(SQL);
        
        try {
            while(rs.next()) {
                String NIS = rs.getString(1);
                String Name = rs.getString(2);
                String Gender = "";
                if ("M".equals(rs.getString(3))) {
                    Gender = "Male";
                } else {
                    Gender = "Female";
                }
                String Birthplace = rs.getString(4);
                String Birthdate = rs.getString(5);
                String Grade = rs.getString(6);
                String Email = rs.getString(7);
                String Address = rs.getString(8);
                String data[] = {NIS, Name, Gender, Birthplace, Birthdate, Grade, Email, Address};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(formsis.class.getName()).log(Level.SEVERE, null, ex);
        } tb1.setModel(dtm);
    }
